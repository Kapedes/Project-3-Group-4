// Function to plot the first bubble chart using Plotly.js
function plotBubbleChart1(budgets, ratings, earnings) {
  // Calculate profit for each data point

  const trace = {
    x: earnings,
    y: budgets,
    mode: 'markers',
    marker: {
      size: markerSizes,
      sizemode: 'diameter',
      sizeref: 50000,
      color: ratings,
      colorscale: 'Viridis',
      showscale: true
    },
    type: 'scatter',
    text: ratings.map((rating, index) => `<br>Title: ${titles[index]}<br>Rating: ${rating} out of 10 <br>Earnings:  $${Math.round(earnings[index]/1000000)}m<br>Budget: $${budgets[index]/1000000}m<br>`)
  };

  const layout = {
    title: 'Budget vs Earnings (Bubble size: Ratings)',
    xaxis: {
      title: 'Earnings',
      range: [0, undefined]
    },
    yaxis: {
      title: 'Budget',
      range: [0, undefined]
    }
  };

  const data = [trace];

  Plotly.newPlot('bubbleChartContainer1', data, layout);
}

// Plot the first bubble chart after function definition
plotBubbleChart1(budgets, ratings, earnings);  








